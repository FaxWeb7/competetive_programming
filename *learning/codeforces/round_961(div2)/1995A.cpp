void solve(){
    int n, k; cin >> n >> k;
    if (k == 0) {cout << "0\n"; return;}

    int ans = 1;
    k -= n, n--;
    while (k > 0){
        for (int i = 0; i < 2; ++i){
            if (k <= 0) break;
            k -= n;
            ans++;
        }
        n--;
    }

    cout << ans << '\n';
}

