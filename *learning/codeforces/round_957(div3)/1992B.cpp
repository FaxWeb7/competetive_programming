void solve(){
    int n, k; cin >> n >> k;

    int mx = 0;
    int s2 = 0;
    for (int i = 0; i < k; ++i){
        int x; cin >> x;
        s2 += x-1;
        mx = max(mx, x);
    }

    s2 -= mx-1;
    cout << s2 + (n-mx) << '\n';
}

